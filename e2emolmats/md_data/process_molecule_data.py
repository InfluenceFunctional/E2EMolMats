def extract_molecule_data(mol_block):
    indices, types, charges, coordinates = [], [], [], []
    for ind, line in enumerate(mol_block.split('\n')):
        splitline = line.split(' ')
        indices.append(splitline[0])
        types.append(splitline[1])
        charges.append(splitline[2])
        coordinates.append(', '.join(splitline[3:6]))

    charges = np.genfromtxt(charges)
    coordinates = np.genfromtxt(coordinates, delimiter=', ')

    return {'charges': charges, 'coordinates': coordinates, 'indices': indices, 'types': types}


